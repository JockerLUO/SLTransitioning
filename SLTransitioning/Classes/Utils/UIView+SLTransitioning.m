#import "UIView+SLTransitioning.h"

@implementation UIView (SLTransitioning)

- (CGFloat)sl_x {
    return self.frame.origin.x;
}

- (void)setSl_x:(CGFloat)x {
    CGRect rect = self.frame;
    rect.origin.x = x;
    [self setFrame:rect];
}

- (CGFloat)sl_y {
    return self.frame.origin.y;
}

- (void)setSl_y:(CGFloat)y {
    CGRect rect = self.frame;
    rect.origin.y = y;
    [self setFrame:rect];
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_width {
    return CGRectGetWidth(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_width:(CGFloat)width {
    CGRect frame = self.frame;
    frame.size.width = width;
    self.frame = frame;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_height {
    return CGRectGetHeight(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_height:(CGFloat)height {
    CGRect frame = self.frame;
    frame.size.height = height;
    self.frame = frame;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_left {
    return CGRectGetMinX(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_left:(CGFloat)x {
    CGRect frame = self.frame;
    frame.origin.x = x;
    self.frame = frame;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_top {
    return CGRectGetMinY(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_top:(CGFloat)y {
    CGRect frame = self.frame;
    frame.origin.y = y;
    self.frame = frame;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_right {
    return CGRectGetMaxX(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_right:(CGFloat)right {
    CGRect frame = self.frame;
    frame.origin.x = right - frame.size.width;
    self.frame = frame;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_bottom {
    return CGRectGetMaxY(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_bottom:(CGFloat)bottom {
    CGRect frame = self.frame;
    frame.origin.y = bottom - frame.size.height;
    self.frame = frame;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_centerX {
    return CGRectGetMidX(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_centerX:(CGFloat)centerX {
    self.center = CGPointMake(centerX, self.sl_centerY);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGFloat)sl_centerY {
    return CGRectGetMidY(self.frame);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_centerY:(CGFloat)centerY {
    self.center = CGPointMake(self.sl_centerX, centerY);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGPoint)sl_origin {
    return self.frame.origin;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_origin:(CGPoint)origin {
    CGRect frame = self.frame;
    frame.origin = origin;
    self.frame = frame;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (CGSize)sl_size {
    return self.frame.size;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setSl_size:(CGSize)size {
    CGRect frame = self.frame;
    frame.size = size;
    self.frame = frame;
}

- (CGPoint)sl_position {
    return self.frame.origin;
}

- (void)setSl_position:(CGPoint)position {
    CGRect rect = self.frame;
    rect.origin = position;
    [self setFrame:rect];
}

@end
